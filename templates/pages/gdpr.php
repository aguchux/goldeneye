    Fundamental rights <br>

The       <a id="u6185-4" href="info/aid-development-cooperation-fundamental-rights/your-rights-eu/eu-charter-fundamental-rights_en.html" data-muse-uid="U6185" data-muse-type="txt_frame" data-ibe-flags="txtStyleSrc"><!-- content -->EU Charter of Fundamental Rights</a>                                                  stipulates that EU citizens have the right to protection of their personal data. <br>

Protection of personal data <br><br>

Legislation<br>

The data protection package adopted in May 2016 aims at making Europe fit for the digital age. More than 90% of Europeans say they want the same data protection rights across the EU and regardless of where their data is processed.
<br><br>
The General Data Protection Regulation (GDPR)<br>

                               <a id="u6218-4" href="legal-content/EN/TXT/index.htm?qid=1532348683434&amp;uri=CELEX:02016R0679-20160504" data-muse-uid="U6218" data-muse-type="txt_frame" data-ibe-flags="txtStyleSrc"><!-- content -->Regulation (EU) 2016/679</a>            on the protection of natural persons with regard to the processing of personal data and on the free movement of such data. This text includes the corrigendum published in the OJEU of 23 May 2018.

 <br>

The regulation is an essential step to strengthen individuals' fundamental rights in the digital age and facilitate business by clarifying rules for companies and public bodies in the digital single market. A single law will also do away with the current fragmentation in different national systems and unnecessary administrative burdens.
<br>
 

The regulation entered into force on 24 May 2016 and applies since 25 May 2018. <a id="u6221-4" href="info/index.htm" data-muse-uid="U6221" data-muse-type="txt_frame" data-ibe-flags="txtStyleSrc"><!-- content -->More information for companies and individuals.</a>
<br>
 

Information about the incorporation of the General Data Protection Regulation (GDPR) into the <a id="u6224-4" href="About-EFTA/news/Incorporation-General-Data-Protection-Regulation-GDPR-EEA-Agreement-and-continued-application-Directive-9546EC-508856.html" data-muse-uid="U6224" data-muse-type="txt_frame" data-ibe-flags="txtStyleSrc"><!-- content -->EEA Agreement.</a>
<br>
<br>

EU Member States notifications to the European Commission under the GDPR<br>

EU Member States must notify to the Commission certain provisions of their law taken in order to comply with the GDPR.
<br>
<br>

The Data Protection Law Enforcement Directive
<br>
<a id="u6254-4" href="legal-content/EN/TXT/index.htm?qid=1532348683434&amp;uri=CELEX:02016R0679-20160504" data-muse-uid="U6254" data-muse-type="txt_frame" data-ibe-flags="txtStyleSrc"><!-- content -->Directive (EU) 2016/680</a>

                                         on the protection of natural persons regarding processing of personal data connected with criminal offences or the execution of criminal penalties, and on the free movement of such data.
 <br>

The directive protects citizens' fundamental right to data protection whenever personal data is used by criminal law enforcement authorities for law enforcement purposes. It will in particular ensure that the personal data of victims, witnesses, and suspects of crime are duly protected and will facilitate cross-border cooperation in the fight against crime and terrorism.
<br>
 

The directive entered into force on 5 May 2016 and EU countries had to transpose it into their national law by 6 May 2018.
<br>
<br>

National data protection authorities <br>

EU countries have set up            <a id="u6269-4" href="about-edpb/board/members_en.html" data-muse-uid="U6269" data-muse-type="txt_frame" data-ibe-flags="txtStyleSrc"><!-- content -->national bodies</a>               responsible for protecting personal data in accordance with Article 8(3) of the Charter of Fundamental Rights of the EU.
<br>
<br>

European Data Protection Board
<br>

The EU’s national supervisory authorities are currently working together in the framework of the Article 29 Working Party. The European Data Protection Supervisor (EDPS) and the Commission are also members. As of 25 May 2018, the Article 29 Working Party will be replaced by the    <a id="u6284-4" href="legal-content/EN/TXT/index.htm?qid=1532348683434&amp;uri=CELEX:02016R0679-20160504" data-muse-uid="U6284" data-muse-type="txt_frame" data-ibe-flags="txtStyleSrc"><!-- content -->European Data Protection Board (EDPB)</a>                                                              The EDPB has the status of an EU body with legal personality and is provided with an independent secretariat.

 <br>

The EDPB has extensive powers to determine disputes between national supervisory authorities, to give advice and guidance on key concepts of the GDPR and Police Directive.

<br>

<br>
Data Protection in the EU Institutions and Bodies
<br>
Legislation<br>

               <a id="u6299-4" href="about-edpb/board/members_en.html" data-muse-uid="U6299" data-muse-type="txt_frame" data-ibe-flags="txtStyleSrc"><!-- content -->Regulation 45/2001</a>                  sets forth the rules applicable to the processing of personal data by European Union institutions, bodies, offices and agencies. On 23 May 2018, the Council and European Parliament agreed on the new Regulation in order to align the data protection rules for European Union institutions, bodies, offices and agencies with the General Data Protection Regulation (GDPR).

 <br>
<br>
European Data Protection Supervisor<br>

The regulation on the protection of individuals with regard to the processing of personal data by EU institutions established a

                                      <a id="u6305-4" href="about-edpb/board/members_en.html" data-muse-uid="U6305" data-muse-type="txt_frame" data-ibe-flags="txtStyleSrc"><!-- content -->European data protection supervisor (EDPS)</a>                                 The EDPS is an independent EU body responsible for monitoring the application of data protection rules within European Institutions and for investigating complaints.

 
<br>
<br>
Data Protection Officer in the European Commission<br>

The European Commission has appointed a                            <a id="u6308-4" href="about-edpb/board/members_en.html" data-muse-uid="U6308" data-muse-type="txt_frame" data-ibe-flags="txtStyleSrc"><!-- content -->Data Protection Officer</a>            who is responsible for monitoring and the application of data protection rules in the European Commission. The data protection officer independently ensures the internal application of data protection rules in cooperation with the European data protection supervisor.






