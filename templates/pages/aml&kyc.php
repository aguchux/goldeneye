      <p id="u6155-2" class="termsheader">Anti-Money Laundering Policy</p>
   
      <p id="u6155-6">Money laundering is the act of converting money or other monetary instruments gained from illegal activity into money or investments that appear to be legitimate, so that its illegal source cannot be traced. Domestic and international laws that apply to companies, whose customers can deposit and withdraw funds from their accounts.</p>
   
      <p id="u6155-9">Golden Eye Capitals strictly follows the provisions of the anti money laundering and counter terrorism financing policy and demands that its employees fully observe these standards. The Company therefore takes all necessary measures to detect and counter money laundering and terrorism financing. The internal rules are observed in accordance with the relevant international requirements.</p>
    
      <p id="u6155-12">Golden Eye Capitals anti-money laundering procedures</p>
  
      <p id="u6155-15">Implemented Procedures</p>
   
      <p id="u6155-18">The objective of Anti-Money laundering procedures that Golden Eye Capitals implements is to ensure that customers engaging in certain activities are identified to a reasonable standard, while minimizing the compliance burden and impact on legitimate customers. Golden Eye Capitals is committed to assisting governments combat the threat of money laundering and financing terrorist activities around the world. For that purpose Golden Eye Capitals has set up a highly sophisticated electronic system. This system documents and verifies client identification records, and tracks and maintains detailed records of all transactions.</p>
      <p id="u6155-20">Golden Eye Capitals carefully tracks suspicious and significant transaction activities, and reports such activities "providing timely and comprehensive information" to law enforcement bodies. To uphold the integrity of reporting systems and to safeguard businesses, the legislative framework provides legal protection to providers of such information.</p>
      <p id="u6155-22">In order to minimize the risk of money laundering and financing terrorist activities, Golden Eye Capitals neither accepts cash deposits nor disburses cash under any circumstances. Golden Eye Capitals reserves the right to refuse to process a transfer at any stage, where it believes the transfer to be connected in any way to money laundering or criminal activity. It is forbidden for Golden Eye Capitals to inform customers that they have been reported for suspicious activity.</p>
  
      <p id="u6155-25">Clients' identification and verification</p>
     
      <p id="u6155-28">For the purpose of complying with Anti-Money laundering laws, Golden Eye Capitals requires two different documents to verify the identity of the customer. The first document we require is a legal government-issued, identifying document with the picture of the customer on it. It may be a government-issued passport, driver's license (for countries where the driver's license is a primary identification document) or local ID card (no company access cards). The second document we require is a bill with the customer's own name and actual address on it issued 3 months ago at the earliest. It may be a utility bill, a bank statement, an affidavit, or any other document with the customer’s name and address from an internationally recognized organization.</p>
      <p id="u6155-30">Clients are required to submit up to date identification and contact information in a timely manner, as soon as changes occur.</p>
      <p id="u6155-32">Documents in non-western letters must be translated into English by an official translator; the translation must be stamped and signed by the translator and sent together with the original document with a clear picture of the customer on it.</p>
   
      <p id="u6155-35">Payment Policy: Deposits and Withdrawals</p>
     
      <p id="u6155-38">Golden Eye Capitals requires all deposits to come from the sender, whose name is matching the name of the customer in Company’s records. Third party payments are not accepted.</p>
      <p id="u6155-40">To make a withdrawal from a trading account to one of these systems, a correspondent demand must be sent via Client’s Cabinet. Money will be loaded into the wallet within 3 business days. If money has been lost when trading, it cannot be reimbursed by means of a chargeback. Please read the risks disclosure before you start trading:&nbsp; https://www.Golden Eye Capitals/riskdisclosure/.</p>
      <p id="u6155-42">Funds may be withdrawn only to the same account and by the same way it was deposited. The name of the recipient must exactly match the name of the customer in Company’s records. If the deposit was made by wire transfer, funds may be withdrawn only by wire transfer to the same bank and to the same account from which it originated. If the deposit was made by means of electronic currency transfer, funds may be withdrawn only by the means of electronic currency transfer through the same system and to the same account from which it originated.</p>

      <p id="u6155-45">Please be aware that chargebacks to any payment system and bank cards are prohibited.</p>
      <p id="u6155-47">The Company accepts deposits in the following currencies: EUR, USD, GBP, RUB, JPY, CHF, CAD, AUD, NZD and others.</p>
